            for _script in _scripts:
                exec(compile(_script.read_text(encoding="utf8"), _script.name, "exec"), _namespace)  # pylint: disable=exec-used
            bootstrap.load_verbs(_repo, "moo.bootstrap.zork1_verbs", replace=False)
