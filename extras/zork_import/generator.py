def _gen_verb_stub(name: str, routine: ZilRoutine, obj_atom: str | None, is_room: bool) -> str:
    on_target = "$zork_room" if is_room else (f"${obj_atom.lower()}" if obj_atom else "$zork_thing")
    verb_name_clean = name.lower().replace("_", "-")
    body_preview = (routine.raw_body[:300] + "...") if len(routine.raw_body) > 300 else routine.raw_body
    return textwrap.dedent(f"""\
        #!moo verb {verb_name_clean} --on {on_target}
        # pylint: disable=undefined-variable
        # ZIL ACTION stub generated from dungeon.zil / actions.zil
        # Original routine head: {body_preview}
        # TODO: implement this action routine
        print(f"[{{verb_name}}] not yet implemented.")
    """)


